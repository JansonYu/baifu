    	var _left = parseInt($('.nt-container').css('left')) -275 ,
    		$node = $('.nt-container'),
    		$first = $node.find('.nt').first(),
    		$clone = $first.clone(true);

    	$node.append($clone);
    	$node.animate({'left':_left},function ()
    	{
    		$first.remove();
			$node.css({'left':_left+275});
